let selectedTimezone = '',
    userUrl = ''

let intervalId
// Functions
const showAlert = alert => {
    alert.style.transition = 'all .3s ease'
    alert.style.top = '60px'
    alert.style.opacity = '1'
    alert.style.visibility = 'visible'

    return new Promise(resolve => {
        setTimeout(() => {
            alert.style.transition = 'all .5s linear'
            alert.style.top = '0'
            alert.style.opacity = '0'
            alert.style.visibility = 'hidden'
            resolve()
        }, 1000)
    })
}

function getCurrentTimeInTimeZone(timeZone) {
    const date = new Date()
    const options = {
        timeZone: timeZone,
        hour: '2-digit',
        minute: '2-digit',
        hour12: false
    }
    const formatter = new Intl.DateTimeFormat([], options)
    const timeParts = formatter.formatToParts(date)
    const formattedTime = timeParts.map(part => part.value).join('')

    return formattedTime
}

const updateTime = () => currentTimeSpan.innerText = getCurrentTimeInTimeZone(selectedTimezone)

const getUserInfo = () => fetch('https://timescrapy.com/get-user-info'),
      changeSettings = (newTimezone, newUrl) => fetch('https://timescrapy.com/settings', {
        method: 'POST',
        body: JSON.stringify({
            timezone: newTimezone,
            calendly_url: newUrl
        }),
        headers: {
            'Content-type': 'application/json'
        }
      }),
      insertAvatar = imgUrl => userAvatar.innerHTML = `<img src="${imgUrl}">`

function changeTimeLive() {
    intervalId = setInterval(() => {
        updateTime()
    }, 60000);
}

// Main
getUserInfo()
    .then(response => response.json())
    .then(data => {
        insertAvatar(data.profile_pic)
        userUrl = data.calendly_url
        selectedTimezone = data.timezone

        timezoneInp.value = selectedTimezone
        userUrlInp.value = userUrl
    
        updateTime()
        changeTimeLive()
    })
    .catch(err => console.log(err))


saveBtn.addEventListener("click", (event) => {
    event.preventDefault()
    if(!timezones.includes(timezoneInp.value)) {
        alertErrText.innerText = 'Incorrect Timezone'
        showAlert(alertError)
        return
    }

    changeSettings(timezoneInp.value, userUrlInp.value)
        .then(response => {
            if(response.ok) return response.json().then(data => {
                showAlert(alertSuccess)
                console.log(data)
                userUrl = data.calendly_url
                selectedTimezone = data.timezone
                updateTime()
                changeTimeLive()
                saveBtn.disabled = true
            })
            else return response.json().then(err => {
                alertErrText.innerText = err.message
                showAlert(alertError)
            })
        })
})

signoutBtn.addEventListener("click", e => {
    e.preventDefault()
    fetch("https://timescrapy.com/signout", {
        method: 'POST',
        headers: {
            'Content-type': 'application/json'
        }
    })
        .then(response => {
            if(response.redirected) {
                window.location.href = response.url
            } else {
                alertErrText.innerText = 'Something went wrong...'
                showAlert(alertError)
            }
        })
})

timezoneInp.addEventListener("input", () => {
    if(timezoneInp.value !== selectedTimezone) saveBtn.disabled = false
    else saveBtn.disabled = true

    const userInput = timezoneInp.value.toLowerCase()
    const suggestions = timezones.filter(timezone => timezone.toLowerCase().includes(userInput))
    timezoneInp.setAttribute("list", "")

    const datalist = document.createElement('datalist')
    datalist.id = 'timezoneSuggestions'
    suggestions.forEach(suggestion => {
        const option = document.createElement('option')
        option.value = suggestion
        datalist.appendChild(option)
    })

    timezoneInp.setAttribute('list', 'timezoneSuggestions')
    timezoneInp.parentNode.insertBefore(datalist, timezoneInp.nextSibling)
})

userUrlInp.addEventListener("input", () => {
    if(userUrlInp.value !== userUrl) saveBtn.disabled = false
    else saveBtn.disabled = true
})
